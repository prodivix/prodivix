import { copyFileSync, readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { resolvePirSchemaPath } from './pir-schema.js';

const __dirname = fileURLToPath(new URL('.', import.meta.url));
const REPO_ROOT = resolve(__dirname, '../../..');
const OUTPUT_PATH = resolve(REPO_ROOT, 'specs/pir/PIR-current.json');
const VERSION_OUTPUT_PATH = resolve(
  REPO_ROOT,
  'specs/pir/PIR-current.version.json'
);
const GO_VERSION_OUTPUT_PATH = resolve(
  REPO_ROOT,
  'apps/backend/internal/platform/pircontract/current_version.generated.go'
);

const schemaPath = resolvePirSchemaPath();
copyFileSync(schemaPath, OUTPUT_PATH);

const source = JSON.parse(readFileSync(schemaPath, 'utf8'));
const currentVersion = source.properties?.version?.const;
if (typeof currentVersion !== 'string' || !currentVersion.trim()) {
  throw new Error('PIR schema must define properties.version.const.');
}
writeFileSync(
  VERSION_OUTPUT_PATH,
  `${JSON.stringify({ version: currentVersion }, null, 2)}\n`,
  'utf8'
);
writeFileSync(
  GO_VERSION_OUTPUT_PATH,
  `// Code generated by packages/shared/scripts/sync-current-pir-schema.js; DO NOT EDIT.\n\npackage pircontract\n\nconst CurrentVersion = ${JSON.stringify(currentVersion)}\n`,
  'utf8'
);

console.log('Synced current PIR schema.');
console.log(`   Schema: ${schemaPath}`);
console.log(`   Output: ${OUTPUT_PATH}`);
console.log(`   Version: ${currentVersion}`);
