import { copyFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  resolveActivatedPirSnapshot,
  resolveCurrentPirSchema,
} from './pir-schema.js';

const __dirname = fileURLToPath(new URL('.', import.meta.url));
const REPO_ROOT = resolve(__dirname, '../../..');
const OUTPUT_PATH = resolve(REPO_ROOT, 'specs/pir/PIR-current.json');
const VERSION_OUTPUT_PATH = resolve(
  REPO_ROOT,
  'specs/pir/PIR-current.version.json'
);
const GO_VERSION_OUTPUT_PATH = resolve(
  REPO_ROOT,
  'apps/backend/internal/platform/pircontract/current_version.generated.go'
);
const GO_SCHEMA_OUTPUT_PATH = resolve(
  REPO_ROOT,
  'apps/backend/internal/platform/pircontract/current_schema.generated.json'
);

const { schemaPath: snapshotPath, version: currentVersion } =
  resolveActivatedPirSnapshot();
copyFileSync(snapshotPath, OUTPUT_PATH);
const { schemaPath: currentSchemaPath } = resolveCurrentPirSchema();
copyFileSync(currentSchemaPath, GO_SCHEMA_OUTPUT_PATH);

writeFileSync(
  GO_VERSION_OUTPUT_PATH,
  `// Code generated by packages/shared/scripts/sync-current-pir-schema.js; DO NOT EDIT.\n\npackage pircontract\n\nconst CurrentVersion = ${JSON.stringify(currentVersion)}\n`,
  'utf8'
);

console.log('Synced current PIR schema.');
console.log(`   Snapshot: ${snapshotPath}`);
console.log(`   Current schema: ${currentSchemaPath}`);
console.log(`   Activation manifest: ${VERSION_OUTPUT_PATH}`);
console.log(`   Version: ${currentVersion}`);
console.log(`   Go schema: ${GO_SCHEMA_OUTPUT_PATH}`);
